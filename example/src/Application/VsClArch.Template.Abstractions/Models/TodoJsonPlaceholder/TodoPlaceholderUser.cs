namespace VsClArch.Template.Application.Models.TodoJsonPlaceholder;

/// <summary>
/// Represents a user from the Todo JSON Placeholder service.
/// </summary>
public class TodoPlaceholderUser
{
    /// <summary>
    /// Gets or sets the name of the user.
    /// </summary>
    public string? Name { get; set; }

    /// <summary>
    /// Gets or sets the username.
    /// </summary>
    public string? Username { get; set; }

    /// <summary>
    /// Gets or sets the email address.
    /// </summary>
    public string? Email { get; set; }
}
